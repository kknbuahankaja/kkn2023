function setPanelData({ data = [], id = 0, type = "item" }) {
	$("#welcome").hide();
	$("#content-item").hide();
	$("#content-petak").hide();

	if (type === "item") {
		const currentData = data.find(function (obj) {
			return obj.id === id;
		});

		document.getElementById("item-title").innerHTML = currentData.title;
		document.getElementById("item-description").innerHTML = currentData.description;
		document.getElementById("item-image").src = currentData.image;
		document.getElementById("item-image").alt = currentData.title;

		$("#content-item").css("display", "flex");
	} else if (type === "petak") {
		const currentData = data.find(function (obj) {
			return obj.id === id;
		});

		document.getElementById("petak-title").innerHTML = currentData.title;
		$("#content-petak").css("display", "flex");
	}
}
